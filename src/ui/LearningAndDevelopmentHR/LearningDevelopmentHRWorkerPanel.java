    private void DisplayTrainees() {
        int selectedRow = trainingsjTable.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select Training");
            return;
        }
        
        Trainings training = (Trainings)trainingsjTable.getValueAt(selectedRow, 2);
        DefaultTableModel model = (DefaultTableModel) traineesjTable.getModel();
        
        model.setRowCount(0);
        for (Trainees trainess : system.getTraineesDirectory().getTraineesList()) {
            if(trainess.getTraining().equals(training)){
                Object[] row = new Object[5];
                row[0] = trainess.getTraining();
                row[1] = trainess.getProject();
                row[2] = trainess;
                row[3] = trainess.getAssignedOn();
                row[4] = trainess.getStatus();
                model.addRow(row);
            }
        }
    }
    
    private void DisplayTrainings(){
        DefaultTableModel model = (DefaultTableModel) trainingsjTable.getModel();
        
        model.setRowCount(0);
        for (Trainings training : system.getTrainingsDirectory().getTrainingsList()) {
            Object[] row = new Object[5];
            row[0] = training.getProject();
            row[1] = training.getProjectManager();
            row[2] = training;
            row[3] = training.getDescription();
            row[4] = training.getStatus();
            model.addRow(row);
        }
    }
    

