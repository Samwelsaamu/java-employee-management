    private void approveTrainingjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approveTrainingjButtonActionPerformed
        int selectedRow = trainingsjTable.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Select Training to Approve.");
            return;
        }
        
        Trainings training = (Trainings)trainingsjTable.getValueAt(selectedRow, 2);
        if(training==null) {
            JOptionPane.showMessageDialog(this, "Select Training First", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        training.setStatus("Approved");
        JOptionPane.showMessageDialog(null, "Training Requested Approved");
        
        dB4OUtil.storeSystem(system);
        DisplayTrainings();
    }//GEN-LAST:event_approveTrainingjButtonActionPerformed

